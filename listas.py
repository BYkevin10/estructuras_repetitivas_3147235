#Lista:
#Comienza y termina con corchetes cuadrados (Brackets [])
#- Cada elemento de la lista se separa con una coma (,)

paises = [
          "Colombia", 
          "Argentina", 
          "Brasil", 
          "Chile", 
          "Peru"
      ]

